# Membuat list dengan angka acak
angka = [12, 45, 2, 8, 19, 27, 6]

# Menggunakan metode sorted() untuk mengurutkan list kecil ke besar dan menyimpannya dalam variabel baru
angka_terurut = sorted(angka)

# Menampilkan list yang sudah diurutkan
print("List yang sudah diurutkan:")
print(angka_terurut)

# List asli tetap tidak berubah
print("List asli:")
print(angka)
